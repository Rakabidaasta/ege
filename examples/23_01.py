def f(start, end):
    if start == end:
        return 1
    if start > end:
        return 0
    
    # 1.  прибавь 2,
    # 2.  умножь на 5.
    return f(start + 2, end) + f(start * 5, end)

# Сколько есть программ, которые число 2 преобразуют в число 50?
print(f(2, 50))


