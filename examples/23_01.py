# У исполнителя Прибавитель две команды, которым присвоены номера:
def f(start, end):
    if start == end:
        return 1
    if start > end:
        return 0

    # 1.  прибавь 1,
    # 2.  увеличь старшую цифру числа на 1.
    return f(start + 1, end) + f(start + 10, end)

# Первая из них увеличивает число на экране на 1, вторая увеличивает на 1 старшую (левую) цифру числа, 
# например число 23 с помощью такой команды превратится в число 33. 
# Если старшая цифра числа равна 9, то вторая команда оставляет это число неизменным. 

# Сколько есть программ, которые число 35 преобразуют в число 57?
